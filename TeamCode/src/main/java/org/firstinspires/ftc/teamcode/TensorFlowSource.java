    public void init(CameraName cam, int hw) {
        // The TFObjectDetector uses the camera frames from the VuforiaLocalizer, so we create that
        // first.


        this.initVuforia(cam);
        this.initTfod(hw);
        Status = 100;
    }
        /** Wait for the game to begin */



    public int checkThree() {
        /** Activate Tensor Flow Object Detection. */
        if (tfod != null) {
            tfod.activate();


            // getUpdatedRecognitions() will return null if no new information is available since
            // the last time that call was made.
            List<Recognition> updatedRecognitions = tfod.getUpdatedRecognitions();
            if (updatedRecognitions != null) {
                // # of objects
                updatedRecognitions.size();
                if (updatedRecognitions.size() == 3) {
                    int goldMineralX = -1;
                    int silverMineral1X = -1;
                    int silverMineral2X = -1;
                    for (Recognition recognition : updatedRecognitions) {
                        if (recognition.getLabel().equals(LABEL_GOLD_MINERAL)) {
                            goldMineralX = (int) recognition.getLeft();
                        } else if (silverMineral1X == -1) {
                            silverMineral1X = (int) recognition.getLeft();
                        } else {
                            silverMineral2X = (int) recognition.getLeft();
                        }
                    }
                    if (goldMineralX != -1 && silverMineral1X != -1 && silverMineral2X != -1) {
                        if (goldMineralX < silverMineral1X && goldMineralX < silverMineral2X) {

                            tfod.shutdown();
                            return 1;  //left
                        } else if (goldMineralX > silverMineral1X && goldMineralX > silverMineral2X) {
                            tfod.shutdown();
                            return 3; //right
                        } else {
                            tfod.shutdown();
                            return 2; //center
                        }
                    }
                }

            }
            else{
                return -1;
            }

        }
        return -4;
    }




    public void run() {
        while (true){
            int st = this.checkThree();
            if (st != -1) {
                this.Status = st;
            }
        }
    }


    /**
     * Initialize the Vuforia localization engine.
     */
    private void initVuforia(CameraName cam) {
        /*
         * Configure Vuforia by creating a Parameter object, and passing it to the Vuforia engine.
         */
        VuforiaLocalizer.Parameters parameters = new VuforiaLocalizer.Parameters();

        parameters.vuforiaLicenseKey = VUFORIA_KEY;
        parameters.cameraName = cam;

        //  Instantiate the Vuforia engine
        vuforia = ClassFactory.getInstance().createVuforia(parameters);

        // Loading trackables is not necessary for the Tensor Flow Object Detection engine.
    }

    /**
     * Initialize the Tensor Flow Object Detection engine.
     */
    private void initTfod(int hw) {
        int tfodMonitorViewId = hw;
        TFObjectDetector.Parameters tfodParameters = new TFObjectDetector.Parameters(tfodMonitorViewId);
        tfod = ClassFactory.getInstance().createTFObjectDetector(tfodParameters, vuforia);
        tfod.loadModelFromAsset(TFOD_MODEL_ASSET, LABEL_GOLD_MINERAL, LABEL_SILVER_MINERAL);
    }
